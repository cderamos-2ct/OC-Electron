#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const rootDir = "/Volumes/Storage/OpenClaw/.antigravity";
const tasksDir = path.join(rootDir, "tasks");
const itemsDir = path.join(tasksDir, "items");
const boardPath = path.join(rootDir, "TASKS.md");
const statusPath = path.join(rootDir, "TASK_STATUS.md");
const indexPath = path.join(tasksDir, "index.json");
const legacyJsonPath = path.join(tasksDir, "tasks.json");

const statusGroups = [
  { title: "Running", match: new Set(["in_progress", "review"]) },
  { title: "Queued", match: new Set(["queued"]) },
  { title: "Blocked", match: new Set(["blocked"]) },
  { title: "Done", match: new Set(["done"]) },
  { title: "Failed", match: new Set(["failed", "cancelled"]) },
];

function parseScalar(raw) {
  const value = raw.trim();
  if (!value) return "";
  if (value === "[]" || value === "{}") return [];
  if (value === "true") return true;
  if (value === "false") return false;
  if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
    try {
      return JSON.parse(value);
    } catch {
      return value.slice(1, -1);
    }
  }
  return value;
}

function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---\n?/);
  if (!match) {
    throw new Error("Task file is missing frontmatter");
  }

  const lines = match[1].split("\n");
  const data = {};
  let activeArrayKey = null;

  for (const line of lines) {
    if (!line.trim()) continue;
    if (line.startsWith("- ") && activeArrayKey) {
      data[activeArrayKey].push(parseScalar(line.slice(2)));
      continue;
    }

    activeArrayKey = null;
    const keyMatch = line.match(/^([A-Za-z0-9_]+):\s*(.*)$/);
    if (!keyMatch) continue;
    const [, key, rawValue] = keyMatch;

    if (!rawValue.trim()) {
      data[key] = [];
      activeArrayKey = key;
      continue;
    }

    data[key] = parseScalar(rawValue);
  }

  return {
    data,
    body: content.slice(match[0].length).trim(),
  };
}

function normalizeTask(task, filePath) {
  const dependsOn = Array.isArray(task.depends_on) ? task.depends_on : [];
  const blockedBy = Array.isArray(task.blocked_by) ? task.blocked_by : [];
  const tags = Array.isArray(task.tags) ? task.tags : [];
  const artifacts = Array.isArray(task.artifacts) ? task.artifacts : [];

  return {
    id: String(task.id || path.basename(filePath, ".md")),
    title: String(task.title || path.basename(filePath, ".md")),
    status: String(task.status || "queued"),
    priority: String(task.priority || "medium"),
    owner_agent: String(task.owner_agent || "unassigned"),
    agent_type: String(task.agent_type || "orchestrator"),
    created_at: String(task.created_at || ""),
    updated_at: String(task.updated_at || ""),
    source: String(task.source || "manual"),
    depends_on: dependsOn,
    blocked_by: blockedBy,
    tags,
    artifacts,
    path: filePath,
  };
}

function readTasks() {
  const fileNames = fs
    .readdirSync(itemsDir)
    .filter((name) => name.endsWith(".md"))
    .sort();

  return fileNames.map((fileName) => {
    const filePath = path.join(itemsDir, fileName);
    const content = fs.readFileSync(filePath, "utf8");
    const parsed = parseFrontmatter(content);
    return normalizeTask(parsed.data, filePath);
  });
}

function statusGlyph(status) {
  return status === "done" ? "[x]" : "[ ]";
}

function sortTasks(tasks) {
  return [...tasks].sort((a, b) => {
    const byPriority =
      ["critical", "high", "medium", "low"].indexOf(a.priority) -
      ["critical", "high", "medium", "low"].indexOf(b.priority);
    if (byPriority !== 0) return byPriority;
    return a.id.localeCompare(b.id);
  });
}

function renderBoard(tasks) {
  const lines = [
    "# TASKS",
    "",
    "> Generated from `.antigravity/tasks/items/*.md`. Do not edit this file directly.",
    "",
  ];

  for (const group of statusGroups) {
    lines.push(`## ${group.title}`);
    const groupTasks = sortTasks(tasks.filter((task) => group.match.has(task.status)));
    if (groupTasks.length === 0) {
      lines.push("- none");
      lines.push("");
      continue;
    }

    for (const task of groupTasks) {
      const meta = [
        `priority:${task.priority}`,
        `owner:${task.owner_agent}`,
        `type:${task.agent_type}`,
        `path:${path.relative(rootDir, task.path)}`,
      ].join(", ");
      lines.push(`- ${statusGlyph(task.status)} \`${task.id}\` — ${task.title} (${meta})`);
    }
    lines.push("");
  }

  return `${lines.join("\n")}\n`;
}

function renderStatus(tasks) {
  const active = sortTasks(
    tasks.filter((task) => ["queued", "in_progress", "review", "blocked"].includes(task.status)),
  );

  const lines = [
    "# Task Status",
    "",
    "## Active Board",
    "",
    "| Task ID | Title | Status | Owner | Notes |",
    "| --- | --- | --- | --- | --- |",
  ];

  if (active.length === 0) {
    lines.push("| none | none | none | none | No active tasks |");
  } else {
    for (const task of active) {
      const notes = [`priority:${task.priority}`, `type:${task.agent_type}`].join(", ");
      lines.push(`| ${task.id} | ${task.title} | ${task.status} | ${task.owner_agent} | ${notes} |`);
    }
  }

  lines.push(
    "",
    "## Notes",
    "",
    "- Canonical task state lives in `.antigravity/tasks/items/`.",
    "- `TASKS.md` and `tasks/index.json` are generated views.",
    "",
  );

  return `${lines.join("\n")}\n`;
}

function renderIndex(tasks) {
  const summary = Object.fromEntries(
    statusGroups.map((group) => [
      group.title.toLowerCase(),
      tasks.filter((task) => group.match.has(task.status)).length,
    ]),
  );

  return {
    version: 2,
    generatedAt: new Date().toISOString(),
    summary,
    tasks,
  };
}

const tasks = readTasks();
fs.writeFileSync(boardPath, renderBoard(tasks));
fs.writeFileSync(statusPath, renderStatus(tasks));
const index = renderIndex(tasks);
fs.writeFileSync(indexPath, `${JSON.stringify(index, null, 2)}\n`);
fs.writeFileSync(legacyJsonPath, `${JSON.stringify(index, null, 2)}\n`);
console.log(`Synced ${tasks.length} task files.`);
